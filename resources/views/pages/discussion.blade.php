	<p>Story after staying away from social media</p>
	<p>In January I deleted all the social media apps from my phone because they were turning me into an idiot.</p>

<p>For months I’d been avoiding engaging with anything challenging or anxiety inducing. Worried about where I’d be living next year? Dive into Instagram. Tax bill prickling at the back of my mind? Open Facebook. That grief I thought I’d processed piercing at me again? Disappear into the realm of likes and follows and push the feelings away. Distract. Binge. Escape.</p>

Whether it was the Pavlovian thrill of the little red circles of like notifications, or a genuine need to connect with others, there was something that kept me – along with 1.86bn other active monthly Facebook users – going back. With social apps so accessible on my smartphone, this had turned into compulsive checking. Statistica research shows that 47% of UK adults use social media every day, and a GlobalWebIndex report found that at the end of last year people were spending a global average of almost two hours a day on social and messaging networks. I was one of them.

Hours of my evenings, train journeys and lunchtimes were spent hopping from one app to another, cruising for attention in the form of likes. I’d open Facebook, then Instagram, then Messenger, and in the time it had taken me to look at the latter two there was a chance that something might have happened on Facebook. So back I’d go and open it again. Then Instagram. Then Messenger. The cycle would continue. It was annoying the hell out of me.

<p>It wasn’t even meaningful attention I was seeking – if social media wasn’t forthcoming, I’d dip into work email, or even my banking app, in the hope of finding something new there. I just craved something – anything – in the form of a new notification. I felt like a frantic lab rat waiting to hear a bell ring.</p>

<p>Daniel Gerrard, family interventionist and founder of Addiction Helper, believes that social media addiction is a process addiction that is similar to gambling: “The more you do it, the more you want to do it, and the more you block out the outside world. So whether you win or lose, you still get that high feeling. And the more you do it, the more you block out what’s going on.”</p>

<p>I didn’t think I had an addiction, just strong habits. I could, however, understand the pull of social media as an escape from the real world.</p>

<p>So I went clean. I took them all off my phone. I’d still use social media on computers, but I wanted to make sure it wasn’t always with me every second and everywhere.</p>

